        paragraphs = soup.find_all('p')
        raw_text = "\n".join([p.get_text() for p in paragraphs if p.get_text(strip=True)])
        cleaned = clean_text(raw_text)

        outfile.write(f"\n\n{'='*40}\n{name.replace('_', ' ')}\n{'='*40}\n")
        outfile.write(cleaned)
